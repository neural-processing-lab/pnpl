# Tasks and the subject prefix that performs each. Only ``rest`` is
# performed by every participant.
TASKS = ["auditory", "rest", "visual"]

AUDITORY_SUBJECT_PREFIX = "A"
VISUAL_SUBJECT_PREFIX = "V"


def is_task_for_subject(subject: str, task: str) -> bool:
    """Return True if ``subject`` performed ``task`` (per the MOUS design)."""
    if task == "rest":
        return True
    if task == "auditory":
        return subject.startswith(AUDITORY_SUBJECT_PREFIX)
    if task == "visual":
        return subject.startswith(VISUAL_SUBJECT_PREFIX)
    return False

